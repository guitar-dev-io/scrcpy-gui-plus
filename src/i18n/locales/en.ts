export type Translations = typeof en

/**
 * Recursively-optional version of a type. Non-English locales are allowed to
 * omit keys; anything missing falls back to English at runtime via the i18n
 * deep-merge.
 */
export type DeepPartial<T> = {
  [K in keyof T]?: T[K] extends object ? DeepPartial<T[K]> : T[K]
}

/** Recursively-optional {@link Translations}. */
export type DeepPartialTranslations = DeepPartial<Translations>
